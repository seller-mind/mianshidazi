// 获取面试官人格的System Prompt
export function getInterviewerSystemPrompt(persona: PersonaType): string {
  const prompts: Record<PersonaType, string> = {
    A: PERSONA_A_SYSTEM,
    B: PERSONA_B_SYSTEM,
    C: PERSONA_C_SYSTEM,
    D: PERSONA_D_SYSTEM,
    E: PERSONA_E_SYSTEM,
  };
  return prompts[persona] || PERSONA_B_SYSTEM;
}

